console.log('-----WHILE-----')
var c = 1
while (c <= 6) {
    console.log(`Contando ... ${c}`)
    c++
}

console.log('-----DO-----')

var c = 1
do {
    console.log(`Contando ... ${c}`)
    c++
} while (c <= 6) 

console.log('-----FOR-----')

for (var c = 1 ; c <= 6; c++) {
    console.log(`Contando ... ${c}`)
}